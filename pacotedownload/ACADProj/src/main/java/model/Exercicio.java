
package model;

public class Exercicio {
    
    private int id_Exercicio;
    private String nome_Exercicio;
    private String obs_Exercicio;
    private String grupo_Exercicio;

    public int getId_Exercicio() {
        return id_Exercicio;
    }

    public void setId_Exercicio(int id_Exercicio) {
        this.id_Exercicio = id_Exercicio;
    }

    public String getNome_Exercicio() {
        return nome_Exercicio;
    }

    public void setNome_Exercicio(String nome_Exercicio) {
        this.nome_Exercicio = nome_Exercicio;
    }

    public String getObs_Exercicio() {
        return obs_Exercicio;
    }

    public void setObs_Exercicio(String obs_Exercicio) {
        this.obs_Exercicio = obs_Exercicio;
    }

    public String getGrupo_Exercicio() {
        return grupo_Exercicio;
    }

    public void setGrupo_Exercicio(String grupo_Exercicio) {
        this.grupo_Exercicio = grupo_Exercicio;
    }
    
    
    
}
